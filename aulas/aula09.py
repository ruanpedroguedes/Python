# operadores matemáticos no python

adicao = 10 + 10
print(adicao)

subtraçao = 20 - 10
print(subtraçao)

multiplicacao = 10 * 10
print(multiplicacao)

divisao = 10 / 2.2
print(divisao)

divisao_inteira = 10 // 2.2
print(divisao_inteira)

exponenciacao = 2 ** 10
print(exponenciacao)

modulo = 55 % 2 
print(modulo)

